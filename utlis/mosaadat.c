#include "../mandatory/push_swap.h"

void	fun(char **av, int ac)
{
	int	i;

	i = 1;
	while (i < ac)
	{
		if (av[i][0] == '\0')
		{
			ft_putstr_fd("Error\n", 2);
			exit(1);
		}
		i++;
	}
}
